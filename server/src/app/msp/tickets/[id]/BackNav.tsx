'use client';

import { useRouter } from 'next/navigation';

export default function BackNav() {
  const router = useRouter();
  
  return (
    <button
      type="button"
      className="bg-gray-200 px-4 py-2 rounded hover:bg-gray-300"
      onClick={() => router.back()}
    >
      Back to Tickets
    </button>
  );
}
